/**
 * Calculate cosine similarity between two vectors
 * Returns a value between 0 and 1, where 1 means identical
 */
function cosineSimilarity(vecA: number[], vecB: number[]): number {
  if (!vecA?.length || !vecB?.length || vecA.length !== vecB.length) {
    return 0;
  }

  let dotProduct = 0;
  let normA = 0;
  let normB = 0;

  for (let i = 0; i < vecA.length; i++) {
    dotProduct += vecA[i] * vecB[i];
    normA += vecA[i] * vecA[i];
    normB += vecB[i] * vecB[i];
  }

  normA = Math.sqrt(normA);
  normB = Math.sqrt(normB);

  if (normA === 0 || normB === 0) return 0;

  return dotProduct / (normA * normB);
}

/**
 * Generate a mock embedding for testing when AI server is not available
 * Creates a deterministic embedding based on the text content
 */
function generateMockEmbedding(text: string): number[] {
  console.log('🧪 [MOCK] Generating mock embedding for testing...');
  
  // Create a simple hash-based embedding (768 dimensions like real embeddings)
  const embedding: number[] = [];
  const dimension = 768;
  
  // Use text content to create deterministic but varied embeddings
  let seed = 0;
  for (let i = 0; i < text.length; i++) {
    seed += text.charCodeAt(i) * (i + 1);
  }
  
  // Generate pseudo-random but deterministic values
  for (let i = 0; i < dimension; i++) {
    const x = Math.sin(seed + i) * 10000;
    embedding.push(x - Math.floor(x));
  }
  
  // Normalize the embedding
  const norm = Math.sqrt(embedding.reduce((sum, val) => sum + val * val, 0));
  const normalized = embedding.map(val => val / norm);
  
  console.log(`🧪 [MOCK] Generated ${dimension}-dimensional mock embedding`);
  return normalized;
}


